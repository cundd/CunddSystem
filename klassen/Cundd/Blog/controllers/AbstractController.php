	/**
	 * indexAction
	 */
	public static function indexAction(){
	    $tableName = Cundd_Request::getPara('table');
	    
	    if($tableName){
		$blog = new CunddBlog($tableName);
	    } else {
		echo 'No table';
	    }
	}


	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/**
	 * The magic method parses the not implemented method's prefix as the name of the table.
	 *
	 * @param string $name
	 * @param array $arguments
	 */
	public static function __callStatic($name, $arguments) {
	    if(strpos($name, 'Action')){
		$tableName = str_replace('Action', '', $name);
		$blog = new CunddBlog($tableName);
	    }
	}
}
