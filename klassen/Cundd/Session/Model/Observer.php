class Cundd_Session_Model_Observer extends Cundd_Core_Model_Observer{
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	// Variablen deklarieren
	
	
	
	const USER_LOGGED_IN 	= '_cundd_user_userLoggedIn';
	const USER_LOGGED_OUT 	= '_cundd_user_userLoggedOut';
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/**
	 * Event-Handler für 'userLoggedIn'.
	 */
	public function userLoggedIn(array $event){
		/* Ein Registry-Eintrag wird gesetzt, der anzeigt, dass gerade ein Benutzer 
		 * angemeldet wurde.
		 */
		$value = true;
		Cundd::registry(self::USER_LOGGED_IN,$value);
	}
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/**
	 * Event-Handler für 'userLoggedOut'.
	 */
	public function userLoggedOut(array $event){
		/* Ein Registry-Eintrag wird gesetzt, der anzeigt, dass gerade ein Benutzer 
		 * abgemeldet wurde.
		 */
		$value = true;
		Cundd::registry(self::USER_LOGGED_OUT,$value);
	}
}
