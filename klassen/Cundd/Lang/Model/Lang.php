class Cundd_Lang_Model_Lang extends Cundd_Lang_Model_Abstract{
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	// Variablen deklarieren
	public $langLibs = array();
	public $langHomeLinks = array();
	public $langCurrentLinks = array();
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/**
	 * Konstruktor
	 */
	public function __construct(){		
		parent::__construct();
		$this->_getAvailableLangLibs();
		$this->_createLangLinks();
	}
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/** 
	 * Die Methode ruft die statische Methode Cundd_Lang::getAvailableLangLibs() auf.
	 * @return array
	 */
	protected function _getAvailableLangLibs(){
		$this->langLibs = Cundd_Lang::getAvailableLangLibs();
		// $this->pd($this->langLibs);
		return $this->langLibs;
	}
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/** 
	 * Die Methode erstellt die Links für das Laden der Seite in einer neuen Sprache. */
	protected function _createLangLinks(){
		$this->_createNewLangHomeLinks();
		$this->_createNewLangCurrentLinks();
	}
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/** 
	 * Die Methode erstellt die Links zur Home-Seite in einer anderen Sprache. */
	protected function _createNewLangHomeLinks(){
		foreach($this->langLibs as $langLib){
			$this->langHomeLinks[] = CunddConfig::__('CunddBaseUrl').'?lang='.$langLib;
		}
		return;
	}
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/** 
	 * Die Methode erstellt die Links zur aktuellen Seite in einer anderen Sprache. */
	protected function _createNewLangCurrentLinks(){
		foreach($this->langLibs as $langLib){
			$this->langCurrentLinks[] = $_SERVER["REQUEST_URI"].'?lang='.$langLib;
		}
		return;
	}
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/** 
	 * Die Methode gibt die gesammelten Informationen und Links in einem Assoziativen Array 
	 * zurück. */
	public function getAll(){
		return array(
			'langLibs' 			=> $this->langLibs,
			'langHomeLinks' 	=> $this->langHomeLinks,
			'langCurrentLinks'	=> $this->langCurrentLinks,
		);
	}
	
	
	
	protected function _isMutable(){
		return (bool) false;
	}
	protected function _isSingleton(){
		return (bool) false;
	}
	protected function _isPersistent(){
		return (bool) false;
	}
	protected function _managedMode(){
		return self::CUNDD_MANAGED_MODE_NONE;
	}
}
