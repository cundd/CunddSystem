	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/**
	 * @see generateAction() 
	 */
	public static function indexAction(array $arguments = array()){
		return self::generateAction($arguments); 
	}
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/**
	 * Die Methode erstellt einen zufälligen Key 
	 */
	public static function generateAction(array $arguments = array()){
		$stringMd5 = md5(uniqid(rand(), true));
		if(count($arguments) > 0){
			if(is_numeric($arguments[0]))$stringMd5 = substr($stringMd5, 0, $arguments[0]);
		}
		return $stringMd5; 
	}
	
	
	
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	//MWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMWMW
	/**
	 * @param string $actionName
	 * @return string
	 */
	public static function infoAction($actionName = NULL){
		$info = '';
		switch($actionName){
			default:
				$info = Cundd_Lang::get('The KEY-plugin provides functions to generate keys of variable length.');
				break;
		}
		return $info;
	}
}
